/// List of all the paths that are used to navigate in temp feature.
///
/// Have to been filled with the entries like this:
/// ```dart
/// /// Path description.
/// static const String screenPath = 'screenPath';
/// ```
abstract class AppRoutePaths {
  /// Path to temp screen.
  static const String tempPath = '/temp';

  /// Path to debug screen.
  static const String debugPath = 'debug';

  /// Path to dash screen.
  static const String dashPath = 'dash';

  /// Path to info screen.
  static const String infoPath = 'info';

  /// Path to logs history screen.
  static const String logHistory = '/logHistory';

  /// Path to onboarding screen.
  static const String onboardingPath = '/onboardingPath';

  /// Path to login screen.
  static const String loginPath = '/loginPath';

  /// Path to home screen.
  static const String homePath = '/homePath';

  /// Path to services screen.
  static const String servicesPath = 'servicesPath';

  /// Path to card screen.
  static const String cardPath = 'cardPath';

  /// Path to chat screen.
  static const String doctorsPath = 'doctorsPath';

  /// Path to main screen.
  static const String mainPath = 'mainPath';

}
